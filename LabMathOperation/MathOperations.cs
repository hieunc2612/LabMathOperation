namespace LabMathOperation
{
    internal class MathOperations
    {

        public int add(int a, int b) 
        {
            return a + b;
        }
        public double add(double a, double b,double c)
        {
            return a + b + c;
        }
        public decimal add(decimal a, decimal b,decimal c)
        {
            return a + b + c;
        }

    }
}